jQuery(function($){
	'use strict';

	// -------------------------------------------------------------
	//   Basic Navigation Hotel
	// -------------------------------------------------------------
	(function () {
		var $frame  = $('#basic-hotel');
		var $wrap   = $frame.parent();

		// Call Sly on frame
		$frame.sly({
			horizontal: 1,
			itemNav: 'basic',
			smart: 1,
			activateOn: 'click',
			mouseDragging: 1,
			touchDragging: 1,
			releaseSwing: 1,
			startAt: 0,
            endAt: 25,
			scrollBar: $wrap.find('.scrollbar'),
			scrollBy: null,
			speed: 300,
			elasticBounds: 1,
			easing: 'easeOutExpo',
			dragHandle: 1,
			dynamicHandle: 1,
			clickBar: 1,

		});
	}());
});

